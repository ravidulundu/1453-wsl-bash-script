# GitHub: Create new project and push to GitHub
# Usage: ghnew <project-name> [--private] [--public]
ghnew() {
    local project_name="\$1"
    local visibility="public"
    
    # Check if gh is installed
    if ! command -v gh &>/dev/null; then
        echo "‚ùå GitHub CLI (gh) kurulu deƒüil!"
        echo "Kurulum i√ßin: gh auth login"
        return 1
    fi
    
    # Check if authenticated
    if ! gh auth status &>/dev/null; then
        echo "‚ùå GitHub'a giri≈ü yapƒ±lmamƒ±≈ü!"
        echo "√ñnce giri≈ü yapƒ±n: gh auth login"
        return 1
    fi
    
    # Validate project name
    if [ -z "\$project_name" ]; then
        echo "‚ùå Proje adƒ± gerekli!"
        echo "Kullanƒ±m: ghnew <proje-adƒ±> [--private|--public]"
        return 1
    fi
    
    # Check for visibility flag
    if [[ "\$2" == "--private" ]]; then
        visibility="private"
    elif [[ "\$2" == "--public" ]]; then
        visibility="public"
    fi
    
    # Check if directory already exists
    if [ -d "\$project_name" ]; then
        echo "‚ö†Ô∏è  Klas√∂r zaten mevcut: \$project_name"
        echo "Devam etmek istiyor musunuz? (y/N)"
        read -r response
        if [[ ! "\$response" =~ ^[yY]\$ ]]; then
            echo "ƒ∞ptal edildi."
            return 1
        fi
        cd "\$project_name" || return 1
    else
        # Create project directory
        mkdir -p "\$project_name" || {
            echo "‚ùå Klas√∂r olu≈üturulamadƒ±!"
            return 1
        }
        cd "\$project_name" || return 1
    fi
    
    echo "üìÅ Proje klas√∂r√º: \$(pwd)"
    
    # Initialize git if not already initialized
    if [ ! -d .git ]; then
        echo "üîß Git ba≈ülatƒ±lƒ±yor..."
        git init || {
            echo "‚ùå Git init ba≈üarƒ±sƒ±z!"
            return 1
        }
    fi
    
    # Create README if doesn't exist
    if [ ! -f README.md ]; then
        echo "üìù README.md olu≈üturuluyor..."
        cat > README.md << READMEEOF
# \$project_name

## A√ßƒ±klama
Bu proje otomatik olarak olu≈üturuldu.

## Kurulum
\\\`\\\`\\\`bash
# Kurulum adƒ±mlarƒ± buraya
\\\`\\\`\\\`

## Kullanƒ±m
\\\`\\\`\\\`bash
# Kullanƒ±m √∂rnekleri buraya
\\\`\\\`\\\`

## Lisans
MIT
READMEEOF
    fi
    
    # Create .gitignore if doesn't exist
    if [ ! -f .gitignore ]; then
        echo "üö´ .gitignore olu≈üturuluyor..."
        cat > .gitignore << GITIGNOREEOF
# Dependencies
node_modules/
venv/
__pycache__/
*.pyc

# Build outputs
dist/
build/
*.log

# IDE
.vscode/
.idea/
*.swp

# OS
.DS_Store
Thumbs.db

# Environment
.env
.env.local
GITIGNOREEOF
    fi
    
    # Add and commit
    echo "üíæ ƒ∞lk commit yapƒ±lƒ±yor..."
    git add . || {
        echo "‚ùå Git add ba≈üarƒ±sƒ±z!"
        return 1
    }
    
    git commit -m "Initial commit: Setup \$project_name" || {
        echo "‚ùå Git commit ba≈üarƒ±sƒ±z!"
        return 1
    }
    
    # Create GitHub repo
    echo "üåê GitHub'da repo olu≈üturuluyor (\$visibility)..."
    if [ "\$visibility" = "private" ]; then
        gh repo create "\$project_name" --private --source=. --push || {
            echo "‚ùå GitHub repo olu≈üturma ba≈üarƒ±sƒ±z!"
            return 1
        }
    else
        gh repo create "\$project_name" --public --source=. --push || {
            echo "‚ùå GitHub repo olu≈üturma ba≈üarƒ±sƒ±z!"
            return 1
        }
    fi
    
    echo ""
    echo "‚úÖ Proje ba≈üarƒ±yla olu≈üturuldu ve GitHub'a g√∂nderildi!"
    echo "üì¶ Repo: https://github.com/\$(gh api user --jq '.login')/\$project_name"
    echo "üìÅ Yerel: \$(pwd)"
}

# GitHub: Quick commit and push
# Usage: ghpush "commit message"
ghpush() {
    local commit_msg="\${1:-"Quick update"}"
    
    # Check if we're in a git repo
    if [ ! -d .git ]; then
        echo "‚ùå Bu bir git repository deƒüil!"
        return 1
    fi
    
    echo "üíæ Deƒüi≈üiklikler commit ediliyor..."
    git add .
    git commit -m "\$commit_msg" || {
        echo "‚ö†Ô∏è  Commit ba≈üarƒ±sƒ±z (deƒüi≈üiklik yok olabilir)"
        return 1
    }
    
    echo "üì§ GitHub'a g√∂nderiliyor..."
    git push || {
        echo "‚ùå Push ba≈üarƒ±sƒ±z!"
        return 1
    }
    
    echo "‚úÖ Ba≈üarƒ±yla g√∂nderildi!"
}

